#include <iostream>
using namespace std;

int main() {
    int a, b, sum;

    // Ask for input
    cout << "Enter first number: ";
    cin >> a;

    cout << "Enter second number: ";
    cin >> b;

    // Calculate sum
    sum = a + b;

    // Display result
    cout << "The sum of " << a << " and " << b << " is " << sum << endl;

    return 0;
}
